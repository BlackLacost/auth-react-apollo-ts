/** All built-in and custom scalars, mapped to their actual values */
export type Scalars = {
  ID: string;
  String: string;
  Boolean: boolean;
  Int: number;
  Float: number;
  /** The `Upload` scalar type represents a file upload. */
  Upload: any;
};

export type AuthPayload = {
  __typename?: 'AuthPayload';
  token: Scalars['String'];
  user: User;
};

export enum CacheControlScope {
  Private = 'PRIVATE',
  Public = 'PUBLIC'
}

export type Mutation = {
  __typename?: 'Mutation';
  login: AuthPayload;
  postCreate: Post;
  signup: AuthPayload;
  vote: Vote;
};


export type MutationLoginArgs = {
  password: Scalars['String'];
  username: Scalars['String'];
};


export type MutationPostCreateArgs = {
  body: Scalars['String'];
  title: Scalars['String'];
};


export type MutationSignupArgs = {
  name: Scalars['String'];
  password: Scalars['String'];
  username: Scalars['String'];
};


export type MutationVoteArgs = {
  postId: Scalars['Int'];
};

export type Post = {
  __typename?: 'Post';
  author: User;
  body: Scalars['String'];
  id: Scalars['ID'];
  title: Scalars['String'];
};

export type Query = {
  __typename?: 'Query';
  posts: Array<Post>;
  users: Array<User>;
};

export type User = {
  __typename?: 'User';
  id: Scalars['ID'];
  name: Scalars['String'];
  posts: Array<Post>;
  username: Scalars['String'];
};

export type Vote = {
  __typename?: 'Vote';
  id: Scalars['ID'];
  post: Post;
  user: User;
};

